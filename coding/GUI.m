% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
clc;
xBaseArr=zeros(1,100);xBaseI=1;
disArr=zeros(1,100);disI=1;
f=4; p=2.8e-3;
try
    [vidOriginal1]=CallCamera(3,handles.axes1);% 原始视频1
    [vidOriginal2]=CallCamera(2,handles.axes3);% 原始视频2
catch
    set(handles.edit7,'string','ERROR:摄像头采集错误');
end
% axes(handles.axes5);
while pi>0
    if get(gcf,'CurrentCharacter')==32    
        delete(gcf);break;
    end 
    [centers1,~,~]=ImgPreprocessing(vidOriginal1);[centers2,~,~]=ImgPreprocessing(vidOriginal2);
    [xmin,ymin,zmin,ballNum]=Matching(centers1,centers2,p,f,handles.edit7);   
    pause(0.03);
    if ballNum>0
        [dis]=plotIn3D(xmin,ymin,zmin,ballNum,handles.edit6);
        pause(0.01);
        if ballNum==1
            if xBaseI<=100
                xBaseArr(xBaseI)=ymin;xBaseI=xBaseI+1;
            else
                plotExpArr(xBaseArr,xBaseI-1,-60);break;
            end
        elseif ballNum==2
            if disI<=100
                disArr(disI)=dis;disI=disI+1;
            else
                plotExpArr(disArr,disI-1,100);break;
            end
        end
    end
    clear frame1;
    clear frame2;
end

